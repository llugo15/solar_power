void configure_pins()
{
	// Configure all RD pins as a digital output
	//LATD = 0;
	TRISE = 0; 

	// Configure PWM for free running mode
    // PWM frequency = 25KHz
	// PWM period = Tcy * prescale * PTPER = (1/fcy) * 1 * 75 = 40us
	PWMCON1 = 0x00FF; // Enable all PWM pairs in complementary mode
	PTCON = 0;
    _PTCKPS = 0;        // prescale=1:1 (0=1:1, 1=1:4, 2=1:16, 3=1:64)
    PTPER = 75;        // 40us PWM period (15-bit period value)
	PTMR = 0;         // Clear 15-bit PWM timer counter

    //deadband gap
    _DTBPS = _DTAPS = 0;        // prescale=1:1 (0=1:1, 1=1:4, 2=1:16, 3=1:64)   
    _DTB = _DTA = 7;         // deadtime value
    
    // Enable
	_PTEN = 1;        // Enable PWM time base
}



